# Arithmetic operations
a = 10
b = 3

print(a + b)  # Output: 13
print(a - b)  # Output: 7
print(a * b)  # Output: 30
print(a / b)  # Output: 3.3333333333333335
print(a // b) # Output: 3
print(a % b)  # Output: 1
print(a ** b) # Output: 1000

#Type Coversion
x = 5
y = float(x)  # Convert integer to float
z = complex(x)  # Convert integer to complex number

print(x,y,z)

